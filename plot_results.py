#!/usr/bin/env python3
"""Draw clear SVG charts from results/summary.json.

The script intentionally uses only the Python standard library so that the
figures can be regenerated on a teacher's machine without installing
matplotlib.
"""

from __future__ import annotations

import json
from pathlib import Path


ROOT = Path(__file__).resolve().parent
SUMMARY = ROOT / "results" / "summary.json"
FIGURES = ROOT / "results" / "figures"

MODES = [
    ("baseline-full-identity", "Baseline"),
    ("selective-disclosure-merkle-vc", "Merkle SD"),
    ("blind-token-minimal-disclosure", "Blind Token"),
]

COLORS = {
    "Baseline": "#d95f02",
    "Merkle SD": "#1b9e77",
    "Blind Token": "#386cb0",
    "metadata": "#7570b3",
    "linkability": "#e7298a",
}


def esc(text: object) -> str:
    return (
        str(text)
        .replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def svg_header(width: int, height: int) -> list[str]:
    return [
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{width}" height="{height}" viewBox="0 0 {width} {height}">',
        '<rect width="100%" height="100%" fill="#ffffff"/>',
        '<style>text{font-family:Arial,"Microsoft YaHei",sans-serif;} .title{font-size:28px;font-weight:700;fill:#222;} .axis{font-size:15px;fill:#333;} .label{font-size:15px;fill:#222;} .value{font-size:16px;font-weight:700;fill:#222;} .note{font-size:13px;fill:#555;}</style>',
    ]


def draw_axes(parts: list[str], x: int, y: int, w: int, h: int, ymax: float, ticks: int = 5) -> None:
    parts.append(f'<line x1="{x}" y1="{y+h}" x2="{x+w}" y2="{y+h}" stroke="#333" stroke-width="2"/>')
    parts.append(f'<line x1="{x}" y1="{y}" x2="{x}" y2="{y+h}" stroke="#333" stroke-width="2"/>')
    for i in range(ticks + 1):
        value = ymax * i / ticks
        yy = y + h - h * i / ticks
        parts.append(f'<line x1="{x-6}" y1="{yy:.1f}" x2="{x+w}" y2="{yy:.1f}" stroke="#e6e6e6" stroke-width="1"/>')
        parts.append(f'<text class="axis" x="{x-12}" y="{yy+5:.1f}" text-anchor="end">{value:.0f}</text>')


def draw_bar_chart(title: str, metric: str, ylabel: str, filename: str, ymax: float | None = None) -> None:
    data = json.loads(SUMMARY.read_text(encoding="utf-8"))["summary_by_mode"]
    values = [(short, float(data[key][metric])) for key, short in MODES]
    if ymax is None:
        ymax = max(v for _, v in values) * 1.18
    width, height = 980, 620
    margin_l, margin_t, chart_w, chart_h = 120, 90, 780, 390
    parts = svg_header(width, height)
    parts.append(f'<text class="title" x="{width/2}" y="45" text-anchor="middle">{esc(title)}</text>')
    parts.append(f'<text class="axis" x="30" y="{margin_t + chart_h/2}" transform="rotate(-90 30 {margin_t + chart_h/2})" text-anchor="middle">{esc(ylabel)}</text>')
    draw_axes(parts, margin_l, margin_t, chart_w, chart_h, ymax)
    gap = 70
    bar_w = (chart_w - gap * 4) / 3
    for i, (name, value) in enumerate(values):
        x = margin_l + gap + i * (bar_w + gap)
        bar_h = chart_h * value / ymax
        y = margin_t + chart_h - bar_h
        parts.append(f'<rect x="{x:.1f}" y="{y:.1f}" width="{bar_w:.1f}" height="{bar_h:.1f}" rx="4" fill="{COLORS[name]}"/>')
        parts.append(f'<text class="value" x="{x+bar_w/2:.1f}" y="{y-10:.1f}" text-anchor="middle">{value:g}</text>')
        parts.append(f'<text class="label" x="{x+bar_w/2:.1f}" y="{margin_t+chart_h+38}" text-anchor="middle">{esc(name)}</text>')
    parts.append('<text class="note" x="120" y="570">Generated by plot_results.py from results/summary.json. Python local measurements; not production ZKP timings.</text>')
    parts.append("</svg>")
    (FIGURES / filename).write_text("\n".join(parts), encoding="utf-8")


def draw_privacy_summary() -> None:
    data = json.loads(SUMMARY.read_text(encoding="utf-8"))["summary_by_mode"]
    personal = [(short, float(data[key]["mean_personal_leakage_bits"])) for key, short in MODES]
    linkable = [(short, float(data[key]["linkable_presentations"])) for key, short in MODES]
    width, height = 1080, 660
    margin_l, margin_t, chart_w, chart_h = 115, 95, 850, 390
    ymax = 230
    parts = svg_header(width, height)
    parts.append(f'<text class="title" x="{width/2}" y="45" text-anchor="middle">Privacy Gain: Personal Leakage vs Linkability</text>')
    draw_axes(parts, margin_l, margin_t, chart_w, chart_h, ymax)
    group_w = chart_w / 3
    bar_w = 75
    for i, ((name, pv), (_, lv)) in enumerate(zip(personal, linkable)):
        gx = margin_l + i * group_w + group_w / 2
        ph = chart_h * pv / ymax
        lh = chart_h * lv / ymax
        parts.append(f'<rect x="{gx-bar_w-8:.1f}" y="{margin_t+chart_h-ph:.1f}" width="{bar_w}" height="{ph:.1f}" rx="4" fill="{COLORS[name]}"/>')
        parts.append(f'<rect x="{gx+8:.1f}" y="{margin_t+chart_h-lh:.1f}" width="{bar_w}" height="{lh:.1f}" rx="4" fill="{COLORS["linkability"]}"/>')
        parts.append(f'<text class="value" x="{gx-bar_w/2-8:.1f}" y="{margin_t+chart_h-ph-10:.1f}" text-anchor="middle">{pv:g}</text>')
        parts.append(f'<text class="value" x="{gx+bar_w/2+8:.1f}" y="{margin_t+chart_h-lh-10:.1f}" text-anchor="middle">{lv:g}</text>')
        parts.append(f'<text class="label" x="{gx:.1f}" y="{margin_t+chart_h+38}" text-anchor="middle">{esc(name)}</text>')
    parts.append(f'<rect x="710" y="88" width="18" height="18" fill="#386cb0"/><text class="axis" x="736" y="103">personal leakage bits</text>')
    parts.append(f'<rect x="710" y="116" width="18" height="18" fill="{COLORS["linkability"]}"/><text class="axis" x="736" y="131">linkable presentations (0-12)</text>')
    parts.append('<text class="note" x="115" y="590">Key point: Merkle selective disclosure hides most fields but remains linkable; blind tokens minimize leakage and remove stable redemption handles.</text>')
    parts.append("</svg>")
    (FIGURES / "privacy_summary.svg").write_text("\n".join(parts), encoding="utf-8")


def draw_latency_with_broken_scale_note() -> None:
    draw_bar_chart(
        "Median Local Runtime by Mode",
        "median_latency_ms",
        "milliseconds",
        "latency_comparison.svg",
        ymax=75,
    )


def main() -> None:
    FIGURES.mkdir(parents=True, exist_ok=True)
    draw_privacy_summary()
    draw_bar_chart(
        "Mean Personal Attribute Leakage",
        "mean_personal_leakage_bits",
        "estimated bits",
        "personal_leakage.svg",
        ymax=230,
    )
    draw_bar_chart(
        "Mean Exposed Fields",
        "mean_exposed_fields",
        "fields",
        "exposed_fields.svg",
        ymax=15,
    )
    draw_latency_with_broken_scale_note()
    print(f"Wrote SVG figures to {FIGURES}")


if __name__ == "__main__":
    main()

